
x = 10;
y = 20;
z = 5;
# Logical AND: True if both conditions are true
if x < y and z < y:
    print("Both conditions are True in the AND operation")

# Logical OR: True if at least one condition is true
if x > y or z < y:
    print("At least one condition is True in the OR operation")

# Logical NOT: Inverts the condition
if not (x > y):
    print("The NOT operation makes the condition True")